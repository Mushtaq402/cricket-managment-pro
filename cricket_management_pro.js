function escapeHtml(value) {
  return String(value ?? '')
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#39;');
}

function isAllowedStreamUrl(url) {
  try {
    const parsed = new URL(url);
    return ['http:', 'https:', 'rtmp:', 'rtmps:'].includes(parsed.protocol);
  } catch (err) {
    return false;
  }
}

