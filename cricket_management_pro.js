// ============ INDEXEDDB WRAPPER ============
class PhotoDatabase {
  constructor() {
    this.dbName = 'CricketAppPhotos';
    this.storeName = 'photos';
    this.db = null;
  }

  async init() {
    return new Promise((resolve, reject) => {
      try {
        const request = indexedDB.open(this.dbName, 1);
        
        request.onerror = () => {
          console.error('IndexedDB init failed:', request.error);
          reject(request.error);
        };
        
        request.onsuccess = () => {
          this.db = request.result;
          resolve();
        };
        
        request.onupgradeneeded = (e) => {
          const db = e.target.result;
          if (!db.objectStoreNames.contains(this.storeName)) {
            db.createObjectStore(this.storeName, { keyPath: 'id', autoIncrement: true });
          }
        };
      } catch (err) {
        console.error('IndexedDB error:', err);
        reject(err);
      }
    });
  }

  async savePhoto(playerId, photoData) {
    return new Promise((resolve, reject) => {
      try {
        const transaction = this.db.transaction([this.storeName], 'readwrite');
        const store = transaction.objectStore(this.storeName);
        const photo = {
          playerId,
          data: photoData,
          timestamp: Date.now()
        };
        
        const request = store.add(photo);
        request.onerror = () => reject(request.error);
        request.onsuccess = () => resolve(request.result);
      } catch (err) {
        reject(err);
      }
    });
  }

  async getPhotoByPlayerId(playerId) {
    return new Promise((resolve, reject) => {
      try {
        const transaction = this.db.transaction([this.storeName], 'readonly');
        const store = transaction.objectStore(this.storeName);
        const request = store.getAll();
        
        request.onerror = () => reject(request.error);
        request.onsuccess = () => {
          const photos = request.result.filter(p => p.playerId === playerId);
          resolve(photos.length > 0 ? photos[0].data : null);
        };
      } catch (err) {
        reject(err);
      }
    });
  }

  async cleanupOldPhotos() {
    return new Promise((resolve, reject) => {
      try {
        const transaction = this.db.transaction([this.storeName], 'readwrite');
        const store = transaction.objectStore(this.storeName);
        const request = store.getAll();
        
        request.onerror = () => reject(request.error);
        request.onsuccess = () => {
          const photos = request.result.sort((a, b) => a.timestamp - b.timestamp);
          if (photos.length > MAX_PHOTOS_IDBX) {
            const toDelete = photos.slice(0, photos.length - MAX_PHOTOS_IDBX);
            toDelete.forEach(photo => {
              store.delete(photo.id);
            });
          }
          resolve();
        };
      } catch (err) {
        reject(err);
      }
    });
  }
}

// ============ STORAGE MONITORING ============
class StorageMonitor {
  static getStorageSize() {
    let size = 0;
    try {
      for (let key in localStorage) {
        if (localStorage.hasOwnProperty(key)) {
          size += localStorage[key].length + key.length;
        }
      }
    } catch (err) {
      console.error('Error calculating storage size:', err);
    }
    return size;
  }

  static getStoragePercent() {
    const size = this.getStorageSize();
    return (size / (5 * 1024 * 1024)) * 100;
  }

  static shouldWarn() {
    return this.getStorageSize() > STORAGE_WARNING_THRESHOLD;
  }

  static showWarning() {
    const size = this.getStorageSize();
    const sizeInMB = (size / (1024 * 1024)).toFixed(2);
    const percent = this.getStoragePercent().toFixed(1);
    console.warn(`Storage warning: ${sizeInMB}MB used (${percent}% of 5MB limit)`);
    
    const warningDiv = document.getElementById('storage-warning');
    if (warningDiv) {
      warningDiv.innerHTML = `
        <div style="background: rgba(255, 107, 107, 0.1); border: 1px solid #ff6b6b; padding: 12px; border-radius: 6px; margin-bottom: 16px; display: flex; justify-content: space-between; align-items: center;">
          <span><strong>Storage Warning:</strong> Using ${sizeInMB}MB of 5MB limit. Old photos will be moved to archive or cleaned up.</span>
          <button onclick="StorageMonitor.cleanupPhotos()" style="background: #ff6b6b; color: white; border: none; padding: 6px 12px; border-radius: 4px; cursor: pointer;">Cleanup</button>
        </div>
      `;
      warningDiv.style.display = 'block';
    }
  }

  static async cleanupPhotos() {
    try {
      const players = loadPlayers();
      let cleaned = 0;
      
      for (const player of players) {
        if (player.photo && player.photo.length > MAX_PHOTO_SIZE) {
          if (idbInstance) {
            await idbInstance.savePhoto(player.id, player.photo);
            player.photo = null;
            cleaned++;
          }
        }
      }
      
      if (cleaned > 0) {
        savePlayers(players);
        console.log(`Cleaned up ${cleaned} photos to IndexedDB`);
      }
      
      if (idbInstance) {
        await idbInstance.cleanupOldPhotos();
      }
    } catch (err) {
      console.error('Cleanup error:', err);
    }
  }
}

