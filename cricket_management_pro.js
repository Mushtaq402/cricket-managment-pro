// ============ PHOTO UPLOAD & COMPRESSION ============
function previewPlayerPhoto() {
  try {
    const fileInput = document.getElementById('add-p-photo');
    if (!fileInput || !fileInput.files[0]) {
      console.warn('No file selected for photo upload');
      return;
    }

    const file = fileInput.files[0];

    // Validate file type
    if (!file.type.startsWith('image/')) {
      alert('Please select a valid image file');
      return;
    }

    // Validate file size (max 5MB before compression)
    if (file.size > 5 * 1024 * 1024) {
      alert('Image file is too large. Please select a smaller image.');
      return;
    }

    const reader = new FileReader();
    
    reader.onerror = () => {
      console.error('FileReader error:', reader.error);
      alert('Failed to read the image file');
    };

    reader.onload = (e) => {
      try {
        const img = new Image();
        
        img.onerror = () => {
          console.error('Image load error');
          alert('Failed to load the image');
        };

        img.onload = () => {
          try {
            // Compress image using canvas
            const canvas = document.createElement('canvas');
            const maxWidth = 200;
            const maxHeight = 200;
            let width = img.width;
            let height = img.height;
            
            if (width > height) {
              if (width > maxWidth) {
                height *= maxWidth / width;
                width = maxWidth;
              }
            } else {
              if (height > maxHeight) {
                width *= maxHeight / height;
                height = maxHeight;
              }
            }
            
            canvas.width = width;
            canvas.height = height;
            const ctx = canvas.getContext('2d');
            if (!ctx) {
              throw new Error('Failed to get canvas context');
            }

            ctx.drawImage(img, 0, 0, width, height);
            
            // Store compressed Base64
            let compressedData = canvas.toDataURL('image/jpeg', 0.8);
            
            // Further compress if exceeds max size
            let quality = 0.8;
            while (compressedData.length > MAX_PHOTO_SIZE && quality > 0.1) {
              quality -= 0.1;
              compressedData = canvas.toDataURL('image/jpeg', quality);
            }
            
            if (compressedData.length > MAX_PHOTO_SIZE) {
              alert('Image could not be compressed enough. Please use a smaller image.');
              return;
            }
            
            playerPhotoData = compressedData;
            
            // Show preview
            const preview = document.getElementById('photo-preview-box');
            if (preview) {
              preview.innerHTML = `<img src="${playerPhotoData}" alt="Player Photo">`;
            }
          } catch (err) {
            console.error('Image compression error:', err);
            alert('Failed to process the image');
          }
        };

        img.src = e.target.result;
      } catch (err) {
        console.error('Image processing error:', err);
        alert('Failed to process the image');
      }
    };

    reader.readAsDataURL(file);
  } catch (err) {
    console.error('Photo preview error:', err);
  }
}

// ============ INPUT VALIDATION ============
function validatePlayerName(name) {
  if (!name || typeof name !== 'string') return false;
  name = name.trim();
  return name.length >= 2 && name.length <= 100 && /^[a-zA-Z0-9\s\-']*$/.test(name);
}

function validateTournamentName(name) {
  if (!name || typeof name !== 'string') return false;
  name = name.trim();
  return name.length >= 2 && name.length <= 100;
}

function validateVenue(venue) {
  if (!venue || typeof venue !== 'string') return false;
  return venue.trim().length >= 2 && venue.trim().length <= 100;
}

function validateTeamName(name) {
  if (!name || typeof name !== 'string') return false;
  name = name.trim();
  return name.length >= 2 && name.length <= 100;
}

function validateOvers(overs) {
  const parsed = parseInt(overs);
  return !isNaN(parsed) && parsed > 0 && parsed <= 120;
}

