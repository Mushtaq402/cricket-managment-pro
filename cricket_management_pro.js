function launchScorecard() {
  try {
    const teamAInput = document.getElementById('score-teamA-name');
    const teamBInput = document.getElementById('score-teamB-name');
    const oversSelect = document.getElementById('score-overs-select');

    const teamA = teamAInput ? teamAInput.value.trim() : '';
    const teamB = teamBInput ? teamBInput.value.trim() : '';
    const overs = oversSelect ? parseInt(oversSelect.value) : 0;
    
    if (!teamA || !teamB) {
      alert('Please enter both team names');
      return;
    }

    if (!validateTeamName(teamA)) {
      alert('Team A name is invalid');
      return;
    }

    if (!validateTeamName(teamB)) {
      alert('Team B name is invalid');
      return;
    }

    if (!validateOvers(overs)) {
      alert('Please select valid overs');
      return;
    }
    
    // Create match object
    const match = {
      id: Date.now(),
      teamA,
      teamB,
      overs,
      date: new Date().toLocaleDateString(),
      innings: [
        {
          team: teamA,
          batsmen: [],
          bowlers: [],
          totalRuns: 0,
          totalWkts: 0,
          legalBalls: 0,
          strikerIdx: 0,
          nonStrikerIdx: 1,
          currentOver: [],
          completedOvers: [],
          extras: {},
          isComplete: false
        }
      ],
      currentInningIdx: 0,
      result: 'In Progress'
    };
    
    // Add players
    const playerInputsA = document.querySelectorAll('#scorer-players-list-A input');
    const playerInputsB = document.querySelectorAll('#scorer-players-list-B input');
    
    playerInputsA.forEach(input => {
      if (input && input.value.trim()) {
        const playerName = input.value.trim();
        if (validatePlayerName(playerName)) {
          match.innings[0].batsmen.push({
            name: playerName,
            runs: 0,
            balls: 0,
            fours: 0,
            sixes: 0,
            dismissed: false,
            dismissal: ''
          });
        }
      }
    });
    
    playerInputsB.forEach(input => {
      if (input && input.value.trim()) {
        const playerName = input.value.trim();
        if (validatePlayerName(playerName)) {
          match.innings[0].bowlers.push({
            name: playerName,
            overs: 0,
            maidens: 0,
            runs: 0,
            wickets: 0
          });
        }
      }
    });
    
    if (match.innings[0].batsmen.length < 2) {
      alert('Please add at least 2 batsmen');
      return;
    }
    
    currentMatch = match;
    currentMatch.currentInning = match.innings[0];
    scoreHistory = [];
    
    // Show active scorer
    const setupEl = document.getElementById('scorer-setup');
    const activeEl = document.getElementById('scorer-active');
    if (setupEl) setupEl.style.display = 'none';
    if (activeEl) activeEl.style.display = 'grid';
    
    // Update UI
    const teamLabel = document.getElementById('score-live-team-label');
    if (teamLabel) teamLabel.textContent = `${teamA} Batting`;
    
    // Populate striker select
    const strikerSelect = document.getElementById('score-striker-select');
    if (strikerSelect) {
      strikerSelect.innerHTML = match.innings[0].batsmen.map(b => `<option value="${b.name}">${b.name}</option>`).join('');
    }
    
    // Populate bowler select
    const bowlerSelect = document.getElementById('score-bowler-select');
    if (bowlerSelect) {
      bowlerSelect.innerHTML = match.innings[0].bowlers.map(b => `<option value="${b.name}">${b.name}</option>`).join('');
    }
    
    updateCreaseDetails();
    updateLiveScore();
    
    // Save match
    const matches = loadMatches();
    if (Array.isArray(matches)) {
      matches.push(cloneMatchForStorage(match));
      saveMatches(matches);
    }

    if (StorageMonitor.shouldWarn()) {
      StorageMonitor.showWarning();
    }
  } catch (err) {
    console.error('Launch scorecard error:', err);
    alert('Failed to launch scorecard. Please try again.');
  }
}

function saveCurrentMatch() {
  try {
    if (!currentMatch || typeof currentMatch.id !== 'number') {
      console.error('Invalid current match:', currentMatch);
      return;
    }

    const matches = loadMatches();
    if (!Array.isArray(matches)) {
      throw new Error('Invalid matches data');
    }

    const idx = matches.findIndex(m => m && m.id === currentMatch.id);
    const matchForStorage = cloneMatchForStorage(currentMatch);
    if (idx !== -1) {
      matches[idx] = matchForStorage;
    } else {
      matches.push(matchForStorage);
    }
    saveMatches(matches);
    
    if (StorageMonitor.shouldWarn()) {
      StorageMonitor.showWarning();
    }
  } catch (err) {
    console.error('Save current match error:', err);
  }
}

function cloneMatchForStorage(match) {
  const matchClone = { ...match };
  delete matchClone.currentInning;
  if (Array.isArray(match.innings)) {
    matchClone.innings = match.innings.map(inning => ({ ...inning }));
  }
  return matchClone;
}

